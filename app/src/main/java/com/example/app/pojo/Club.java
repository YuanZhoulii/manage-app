package com.example.app.pojo;

public class Club {
    private int id;
    private String c_name;
    private String c_date;
    private String declaration;
    private String c_info;
    private String c_rules;
    private String c_img;
    private String c_activity_id;
    private String c_school_id;
    private int flag;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public String getC_date() {
        return c_date;
    }

    public void setC_date(String c_date) {
        this.c_date = c_date;
    }

    public String getDeclaration() {
        return declaration;
    }

    public void setDeclaration(String declaration) {
        this.declaration = declaration;
    }

    public String getC_info() {
        return c_info;
    }

    public void setC_info(String c_info) {
        this.c_info = c_info;
    }

    public String getC_rules() {
        return c_rules;
    }

    public void setC_rules(String c_rules) {
        this.c_rules = c_rules;
    }

    public String getC_img() {
        return c_img;
    }

    public void setC_img(String c_img) {
        this.c_img = c_img;
    }

    public String getC_activity() {
        return c_activity_id;
    }

    public void setC_activity(String c_activity_id) {
        this.c_activity_id = c_activity_id;
    }

    public String getC_school_id() {
        return c_school_id;
    }

    public void setC_school_id(String c_school_id) {
        this.c_school_id = c_school_id;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }
}
